// What will you do if we get some wacky weather?


var iceCreamRain = true;  // variable for if it was raining ice cream.

var sodaRain = true;  // variable for if it was raining soda.


if(iceCreamRain == true || sodaRain === true){
    console.log("Bring out cups, bowls and ice cream toppings! Root beer float anyone?"); // If the weather is wacky, will print to console "Bring out cups, bowls and ice cream toppings! Root beer float anyone?"
}else{
    console.log("Bummer, looks like regular weather."); // If the weather isn't wacky, will print to console "Bummer, looks like regular weather."
}